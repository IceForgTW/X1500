int PS2_Port_SendData(void *dat, int len)
{
	char *lvc = (char*)dat;
	char *lvcode;
	int lvi;

	if(!dat || len < 1) return -1;
//diag_printf("PS2_Port_SendData:%s\n", dat);
	StartPS2KbwSend();

	for(lvi = 0; lvi < len; lvi ++){
		if(lvc[lvi] & 0x80) continue;
		lvcode = (char*)KeyCnvTbl2 + ((int)(lvc[lvi]) << 4);
		while(*lvcode){
			ps2_raw_send_char(*lvcode ++);
		}
	}
	StopPS2KbwSend();
	return len;
}

