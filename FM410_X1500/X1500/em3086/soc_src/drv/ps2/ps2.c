static void SetOutPinToInStat(void)
{
	gpio_set_direction(5, 4, GPIO_GDIR_INPUT);
	gpio_set_direction(5, 5, GPIO_GDIR_INPUT);
}

static void SwitchToExtPS2Dev(void)
{
	SetOutPinToInStat();
	gpio_set_level(5, 17, GPIO_LOW_LEVEL);
}

static void SwitchToIntPS2Dev(void)
{
	gpio_set_level(5, 17, GPIO_HIGH_LEVEL);
}

static int StartPS2KbwSend(void)
{
	int lvcnt = 0;

	if (!ps2_init_flag)
		return 0;

	ResetWatchDog();

	while(lvcnt < 3000) {			//wait clk pin keep high level 3ms
		if (GetCpuDatPinLevel()) {
			lvcnt ++;
			Z_usDelay(1);
		} else{
			ResetWatchDog();
			lvcnt = 0;
		}
	}

	SwitchToIntPS2Dev();
	lvcnt = 0;
	while (lvcnt < 1000) {			//wait clk pin keep high level 1ms
		if (GetCpuDatPinLevel()) {
			lvcnt ++;
			Z_usDelay(1);
		} else {
			ResetWatchDog();
			lvcnt = 0;
		}
	}

	return 0;
}

static int StopPS2KbwSend(void)
{
	if (!ps2_init_flag)
		return 0;

	SwitchToExtPS2Dev();
	return 0;
}

static int ps2_raw_send_char(const char chr)
{
	int lvi;
	int lvmb;		//mask
	int lvchk;		//check bit
	int lverr = 0;
	int lvret = 0;
	int s=0;

	ResetWatchDog();

	if (!ps2_init_flag)
		return 0;

//diag_printf("ps2_raw_send_char:%c, %d\n", chr, chr);
WaitBusFree_RawSend:
	SetOutPinToInStat();
	lvi = 0;
	while (!GetCpuDatPinLevel())	{	
		if(lvi ++ < 1000)
			Z_usDelay(10);
		else
		{
			lvret = -1;
			goto exit_ps2_raw_send_char;
		}
	}

	if(lverr ++ > 20)				//´íÎóÌ«¶à£¬Ì«¾Ã
	{
		lvret = -2;
		goto exit_ps2_raw_send_char;
	}

	lvi = 0;
	while (lvi < 100)
	{
		if (GetCpuDatPinLevel()){
			lvi ++;
			Z_usDelay(1);
		}
		else {
			goto WaitBusFree_RawSend;
		}
	}

	SetClkPinHigh();
	SetDatPinHigh();
	Z_usDelay(6);
	if (!GetCpuDatPinLevel())
	{
		goto WaitBusFree_RawSend;
	}
	lvmb = 1;
	lvchk = 0;
	
	SetDatPinLow();			//send start bit
	Z_usDelay(8);
	SetClkPinLow();
	Z_usDelay(ClkDelay);
	
	while(lvmb <= 0x80)		//send data bit
	{
		SetClkPinHigh();
		Z_usDelay(ClkDelay - 8);
		if(chr & lvmb)
		{
			SetDatPinHigh();
			lvchk ++;
		} else
			SetDatPinLow();
	if (!GetCpuDatPinLevel()){
			goto WaitBusFree_RawSend;
		}
		lvmb = lvmb << 1;
		Z_usDelay(8);
		SetClkPinLow();
		Z_usDelay(ClkDelay);
	}

	SetClkPinHigh();			//send check bit
	Z_usDelay(ClkDelay - 8);
	if(lvchk & 1)
		SetDatPinLow();
	else
		SetDatPinHigh();
	if (!GetCpuDatPinLevel()){
		goto WaitBusFree_RawSend;
	}
	Z_usDelay(8);
	SetClkPinLow();
	Z_usDelay(ClkDelay);

	SetClkPinHigh();			//send stop bit
	Z_usDelay(ClkDelay - 8);
	SetDatPinHigh();
	if (!GetCpuDatPinLevel()){
		goto WaitBusFree_RawSend;
	}
	Z_usDelay(8);
	SetClkPinLow();
	Z_usDelay(ClkDelay);

	SetOutPinToInStat();
	while (!GetCpuDatPinLevel());
exit_ps2_raw_send_char:
	SetOutPinToInStat();

	ResetWatchDog();
	return lvret;
}

