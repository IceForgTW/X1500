int PS2_Port_SendData(void *dat, int len)
{
	char *lvc = (char*)dat;
	char *lvcode;
	int lvi;

	if(!dat || len < 1) return -1;

	StartPS2KbwSend();

	for(lvi = 0; lvi < len; lvi ++)
      {
		if(lvc[lvi] & 0x80) 
                continue;
		lvcode = (char*)KeyCnvTbl2 + ((int)(lvc[lvi]) << 4);

		while(*lvcode)
            {
                ps2_raw_send_char(*lvcode ++);
	      }
	}
	StopPS2KbwSend();
	return len;
}

int PS2_Port_RawSendData(void *dat, int len)
{
	int lvi;
	char *lvc = (char*)dat;
	if(!dat || (len < 1)) return -1;
	StartPS2KbwSend();
	for(lvi = 0; lvi < len; lvi ++)
      {
		ps2_raw_send_char(lvc[lvi]);
	}
	StopPS2KbwSend();
	return len;
}

int PS2_Port_SendStr(const char *str)
{
	char *lvc = (char*)str;
	char *lvcode;
	StartPS2KbwSend();
	while(*lvc){
		if(*lvc & 0x80)
		{
			lvc ++;
			continue;
		}
		lvcode = (char*)KeyCnvTbl2 + ((int)(*lvc) << 4);
		while(*lvcode){
			ps2_raw_send_char(*lvcode ++);
		}
		lvc ++;
	}
	StopPS2KbwSend();
	return 0;
}



static int ps2_dev_write(int id, void *buf, int len)
{
	return PS2_Port_SendData(buf, len);
}



